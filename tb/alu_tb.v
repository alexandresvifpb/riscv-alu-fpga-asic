`timescale 1ns / 1ps

module alu_tb;

    // ==========================================
    // ENTRADAS
    // ==========================================

    reg [3:0] a;
    reg [3:0] b;
    reg [1:0] op;

    // ==========================================
    // SAÍDAS
    // ==========================================

    wire [3:0] result;
    wire carry;
    wire overflow;
    wire zero;

    // ==========================================
    // DUT (Device Under Test)
    // ==========================================

    alu dut (

        .a(a),
        .b(b),
        .op(op),

        .result(result),
        .carry(carry),
        .overflow(overflow),
        .zero(zero)
    );

    // ==========================================
    // TESTES
    // ==========================================

    initial begin

        // Geração de waveform
	$dumpfile("sim/waves/alu_tb.vcd");
        $dumpvars(0, alu_tb);

        $display("======================================");
        $display("Iniciando Teste da ALU");
        $display("======================================");

        $monitor(
            "Tempo=%0t | OP=%b | A=%d | B=%d | RESULT=%d | C=%b | OV=%b | Z=%b",
            $time,
            op,
            a,
            b,
            result,
            carry,
            overflow,
            zero
        );

        // ==========================================
        // TESTE 1 - ADD
        // 2 + 3 = 5
        // ==========================================

        op = 2'b00;
        a  = 4'd2;
        b  = 4'd3;

        #10;

        // ==========================================
        // TESTE 2 - SUB
        // 10 - 4 = 6
        // ==========================================

        op = 2'b01;
        a  = 4'd10;
        b  = 4'd4;

        #10;

        // ==========================================
        // TESTE 3 - AND
        // 1100 & 1010 = 1000
        // ==========================================

        op = 2'b10;
        a  = 4'b1100;
        b  = 4'b1010;

        #10;

        // ==========================================
        // TESTE 4 - OR
        // 1100 | 0011 = 1111
        // ==========================================

        op = 2'b11;
        a  = 4'b1100;
        b  = 4'b0011;

        #10;

        // ==========================================
        // TESTE 5 - ZERO FLAG
        // 4 - 4 = 0
        // ==========================================

        op = 2'b01;
        a  = 4'd4;
        b  = 4'd4;

        #10;

        // ==========================================
        // TESTE 6 - CARRY
        // 15 + 1 = 16
        // ==========================================

        op = 2'b00;
        a  = 4'd15;
        b  = 4'd1;

        #10;

        // ==========================================
        // TESTE 7 - OVERFLOW
        // 7 + 7 = 14
        // ==========================================

        op = 2'b00;
        a  = 4'd7;
        b  = 4'd7;

        #10;

        $display("======================================");
        $display("Testes finalizados!");
        $display("======================================");

        $finish;
    end

endmodule
